<section id="hometext" class="font-bold text-lg mb-32">

      <p>There is a particular kind of silence that happens just before a wheelie.</p>
      <br>
      <p>It’s not the absence of sound — the engine is very much alive beneath you — but a tightening of the world. The bars steady in your hands. The asphalt stretching forward like a promise. Your right wrist waiting, patient and dangerous.</p>
      <br>
      <p>And then you roll it on.</p>
      <br>
      <p>The single-cylinder heartbeat of a supermoto rises from a low thump to a sharp, metallic snarl. Torque doesn’t ask for permission — it arrives. The front forks extend like a bow unstringing, and the horizon tilts. Not violently. Not recklessly. Just enough to remind gravity that today, it is being negotiated with.</p>
      <br>
      <p>The front wheel lifts.</p>
      <br>
      <p>There is no moment quite like that first breath of balance — when the earth loosens its grip and the machine pivots around the rear contact patch, a circle of rubber no wider than your palm. Everything narrows. Clutch, throttle, rear brake — three delicate instruments in a symphony of control. Too much throttle and the sky rushes in. Too much brake and the spell collapses. But in the middle — in that impossibly thin margin — there is something close to flight.</p>
      <br>
      <p>A supermoto makes it theatrical.</p>
      <br>
      <p>The upright stance. The wide bars. The twitch of the chassis as it dances on its long-travel suspension. It doesn’t glide like a sportbike; it plays. The engine pulses through the frame and into your chest, each combustion a reminder that this is controlled chaos — mechanical violence turned into elegance by fingertips and instinct.</p>
      <br>
      <p>You’re no longer riding forward.</p>
      <br>
      <p>You’re riding upward.</p>
      <br>
      <p>Street signs pass at an angle. The world becomes sky and road in uneven halves. Wind presses against your jacket, louder now, wrapping around your helmet in a rushing roar. The front wheel spins freely, unburdened, humming in the air like a secret only you and the bike understand.</p>
      <br>
      <p>Balance point.</p>
      <br>
      <p>That sacred place where the throttle barely needs to move. Where the rear brake hovers under your boot like a quiet guardian. Where the engine note settles into a steady, confident growl — not screaming, not straining — just holding you there. Suspended. Defiant.</p>
      <br>
      <p>Time stretches.</p>
      <br>
      <p>A few seconds feel enormous. Your senses sharpen: the subtle drift of the rear tire, the micro-corrections in your wrists, the faint oscillation of the bars. You’re not fighting the bike. You’re conversing with it. A language spoken in millimeters and muscle memory.</p>
      <br>
      <p>The supermoto doesn’t care about lap times or straight-line records in that moment. It cares about attitude. About posture. About the art of lifting something that was designed to stay grounded and making it feel natural.</p>
      <br>
      <p>Eventually, gravity reclaims its due.</p>
      <br>
      <p>You ease off the throttle. A gentle brush of rear brake. The front wheel lowers with a soft, controlled nod — not a slam, but a handshake with the road. Two wheels again. Ordinary physics restored.</p>
      <br>
      <p>But something lingers.</p>
      <br>
      <p>A warmth in your chest. A grin inside your helmet. The quiet knowledge that for a stretch of asphalt and a handful of heartbeats, you and a snarling machine bent the rules together.</p>
      <br>
      <p>Doing a wheelie on a supermoto isn’t just about showing off. It’s about finding that razor-thin edge between control and chaos — and choosing to live there, if only for a moment.</p>

